<?php

namespace ProjectBundle\Entity;

/**
 * Reservation
 */
class Reservation
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var \DateTime
     */
    private $startDate;

    /**
     * @var \DateTime
     */
    private $endDate;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set startDate
     *
     * @param \DateTime $startDate
     *
     * @return Reservation
     */
    public function setStartDate($startDate)
    {
        $this->startDate = $startDate;

        return $this;
    }

    /**
     * Get startDate
     *
     * @return \DateTime
     */
    public function getStartDate()
    {
        return $this->startDate;
    }

    /**
     * Set endDate
     *
     * @param \DateTime $endDate
     *
     * @return Reservation
     */
    public function setEndDate($endDate)
    {
        $this->endDate = $endDate;

        return $this;
    }

    /**
     * Get endDate
     *
     * @return \DateTime
     */
    public function getEndDate()
    {
        return $this->endDate;
    }
    /**
     * @var integer
     */
    private $daysNumber;


    /**
     * Set daysNumber
     *
     * @param integer $daysNumber
     *
     * @return Reservation
     */
    public function setDaysNumber($daysNumber)
    {
        $this->daysNumber = $daysNumber;

        return $this;
    }

    /**
     * Get daysNumber
     *
     * @return integer
     */
    public function getDaysNumber()
    {
        return $this->daysNumber;
    }
    /**
     * @var \DateTime
     */
    private $startTime;

    /**
     * @var \DateTime
     */
    private $endTime;


    /**
     * Set startTime
     *
     * @param \DateTime $startTime
     *
     * @return Reservation
     */
    public function setStartTime($startTime)
    {
        $this->startTime = $startTime;

        return $this;
    }

    /**
     * Get startTime
     *
     * @return \DateTime
     */
    public function getStartTime()
    {
        return $this->startTime;
    }

    /**
     * Set endTime
     *
     * @param \DateTime $endTime
     *
     * @return Reservation
     */
    public function setEndTime($endTime)
    {
        $this->endTime = $endTime;

        return $this;
    }

    /**
     * Get endTime
     *
     * @return \DateTime
     */
    public function getEndTime()
    {
        return $this->endTime;
    }
    /**
     * @var \ProjectBundle\Entity\Client
     */
    private $client;


    /**
     * Set client
     *
     * @param \ProjectBundle\Entity\Client $client
     *
     * @return Reservation
     */
    public function setClient(\ProjectBundle\Entity\Client $client = null)
    {
        $this->client = $client;

        return $this;
    }

    /**
     * Get client
     *
     * @return \ProjectBundle\Entity\Client
     */
    public function getClient()
    {
        return $this->client;
    }
}
